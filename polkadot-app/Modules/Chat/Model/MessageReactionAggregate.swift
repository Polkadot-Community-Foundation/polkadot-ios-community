import Foundation

extension Chat {
    struct MessageReactionAggregate: Equatable {
        let messageId: String
        let emoji: String
        let count: Int
        let reactedByCurrentUser: Bool
        let reactors: [Reactor]

        struct Reactor: Equatable {
            let origin: Chat.LocalMessage.Origin
            let timestamp: Timestamp
        }
    }
}

// MARK: - Static functions

extension Chat.MessageReactionAggregate {
    static func aggregate(
        reactions: [Chat.MessageReaction],
        currentUserOrigin: Chat.LocalMessage.Origin
    ) -> [Chat.MessageReactionAggregate] {
        let grouped = Dictionary(grouping: reactions) { $0.emoji }

        return grouped.map { emoji, reactions in
            let reactedByCurrentUser = reactions.contains { $0.origin == currentUserOrigin }
            let reactors = reactions.map { reaction in
                Reactor(origin: reaction.origin, timestamp: reaction.timestamp)
            }
            .sorted { $0.timestamp < $1.timestamp }

            return Chat.MessageReactionAggregate(
                messageId: reactions.first?.messageId ?? "",
                emoji: emoji,
                count: reactions.count,
                reactedByCurrentUser: reactedByCurrentUser,
                reactors: reactors
            )
        }
        .sorted { lhs, rhs in
            if lhs.count != rhs.count {
                return lhs.count > rhs.count
            }
            return (lhs.reactors.first?.timestamp ?? 0) < (rhs.reactors.first?.timestamp ?? 0)
        }
    }

    static func aggregateByMessage(
        reactions: [String: [Chat.MessageReaction]],
        currentUserOrigin: Chat.LocalMessage.Origin
    ) -> [String: [Chat.MessageReactionAggregate]] {
        reactions.mapValues { messageReactions in
            aggregate(reactions: messageReactions, currentUserOrigin: currentUserOrigin)
        }
    }
}

